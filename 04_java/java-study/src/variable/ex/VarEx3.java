package variable.ex;

public class VarEx3 {
    public static void main(String[] args) {
        // long 타입
        long longVar = 10000000000L; // 리터럴은 기본이 int, 뒤에 L을 붙임
        System.out.println(longVar);

        // boolean 타입
        boolean boolVar = true;
        System.out.println(boolVar);
    }
}
