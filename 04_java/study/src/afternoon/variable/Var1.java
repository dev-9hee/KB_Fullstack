package afternoon.variable;

public class Var1 {
    public static void main(String[] args) {
        int a = 10;
        double b = 4.3;
        boolean c = true;
        char d = 'c'; // 반드시 단 따옴표!!
        String e = "으아아 집 가고 싶다.";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
